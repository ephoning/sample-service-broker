CREATE TABLE IF NOT EXISTS ServiceInstance (
   SERVICE_INSTANCE_ID   VARCHAR(128) NOT NULL,

   SERVICE_ID   VARCHAR(64) NOT NULL,
   PLAN_ID   VARCHAR(64) NOT NULL,
   ORGANIZATION_GUID   VARCHAR(128) NOT NULL,
   SPACE_GUID   VARCHAR(128) NOT NULL,
   DASHBOARD_URL   VARCHAR(128) NOT NULL,

   PRIMARY KEY (SERVICE_INSTANCE_ID)
);

CREATE TABLE IF NOT EXISTS ServiceInstanceBinding (
   SERVICE_INSTANCE_BINDING_ID   VARCHAR(128) NOT NULL,

   SERVICE_INSTANCE_ID   VARCHAR(128) NOT NULL,
   CREDENTIALS   VARCHAR(512) NOT NULL,
   SYSLOG_DRAIN_URL   VARCHAR(128) NOT NULL,
   APP_GUID   VARCHAR(128) NOT NULL,

   PRIMARY KEY (SERVICE_INSTANCE_BINDING_ID),

   FOREIGN KEY(SERVICE_INSTANCE_ID) REFERENCES ServiceInstance(SERVICE_INSTANCE_ID)
);
